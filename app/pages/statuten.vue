    <h2>§ 1 Name, Sitz und Tätigkeitsbereich</h2>
    <ol>
      <li>Der Verein führt den Namen „O.H. Beach Volleyballverein".</li>
      <li>
        Er hat seinen Sitz in Offenhausen und erstreckt seine Tätigkeit vorwiegend
        auf die Gemeinde Offenhausen sowie auf das gesamte österreichische
        Bundesgebiet (für Turnierbesuche).
      </li>
      <li>Die Errichtung von Zweigvereinen ist nicht beabsichtigt.</li>
    </ol>

    <h2>§ 2 Zweck</h2>
    <p>
      Der Verein, dessen Tätigkeit nicht auf Gewinn gerichtet ist, bezweckt die
      Förderung und Pflege des Beachvolleyballsports, insbesondere durch die
      Organisation der jährlichen Ortsmeisterschaft in Offenhausen, die Förderung des
      Nachwuchses durch Training sowie die Stärkung der Beachvolleyball-Community und
      des sozialen Zusammenhalts.
    </p>

    <h2>§ 3 Mittel zur Erreichung des Vereinszwecks</h2>
    <h3>(1) Ideelle Mittel</h3>
    <ul>
      <li>Abhaltung von Trainingsstunden und Übungseinheiten.</li>
      <li>Durchführung von Sportveranstaltungen und Turnieren (z.B. Ortsmeisterschaft).</li>
      <li>Errichtung, Instandhaltung und Pflege der Sportanlagen (Court und Hütte) und Gerätschaften.</li>
      <li>Versammlungen und gesellige Zusammenkünfte zur Förderung der Gemeinschaft.</li>
    </ul>
    <h3>(2) Materielle Mittel</h3>
    <ul>
      <li>Beitrittsgebühren und Mitgliedsbeiträge.</li>
      <li>Erträgnisse aus Veranstaltungen und vereinseigenen Unternehmungen (z.B. Kantine).</li>
      <li>Spenden, Sammlungen, Vermächtnisse und sonstige Zuwendungen.</li>
      <li>Sponsoringgelder und Subventionen.</li>
    </ul>

    <h2>§ 4 Arten der Mitgliedschaft</h2>
    <ol>
      <li>
        Die Mitglieder des Vereins gliedern sich in ordentliche, außerordentliche und
        Ehrenmitglieder.
      </li>
      <li>
        Ordentliche Mitglieder sind jene, die sich voll an der Vereinsarbeit
        beteiligen. Außerordentliche Mitglieder sind solche, die die Vereinstätigkeit
        vor allem durch Zahlung eines erhöhten Mitgliedsbeitrags fördern.
        Ehrenmitglieder sind Personen, die hiezu wegen besonderer Verdienste um den
        Verein ernannt werden.
      </li>
    </ol>

    <h2>§ 5 Erwerb der Mitgliedschaft</h2>
    <ol>
      <li>
        Mitglieder des Vereins können alle physischen Personen, die an der
        Verwirklichung des Vereinszwecks interessiert sind, sowie juristische Personen
        und rechtsfähige Personengesellschaften werden.
      </li>
      <li>
        Über die Aufnahme von ordentlichen und außerordentlichen Mitgliedern
        entscheidet der Vorstand. Die Aufnahme kann ohne Angabe von Gründen verweigert
        werden.
      </li>
    </ol>
    <p>
      Bis zur Entstehung des Vereins erfolgt die vorläufige Aufnahme von ordentlichen
      und außerordentlichen Mitgliedern durch die Vereinsgründer, im Fall eines
      bereits bestellten Vorstands durch diesen. Diese Mitgliedschaft wird erst mit
      Entstehung des Vereins wirksam. Wird ein Vorstand erst nach Entstehung des
      Vereins bestellt, erfolgt auch die (definitive) Aufnahme ordentlicher und
      außerordentlicher Mitglieder bis dahin durch die Gründer des Vereins.
    </p>
    <p>
      Die Ernennung zum Ehrenmitglied erfolgt auf Antrag des Vorstands durch die
      Generalversammlung.
    </p>

    <h2>§ 6 Beendigung der Mitgliedschaft</h2>
    <ol>
      <li>
        Die Mitgliedschaft erlischt durch Tod, bei juristischen Personen und
        rechtsfähigen Personengesellschaften durch Verlust der Rechtspersönlichkeit,
        durch freiwilligen Austritt und durch Ausschluss.
      </li>
      <li>
        Der Austritt kann nur zum 31. Dezember (Jahresende) erfolgen. Er muss dem
        Vorstand mindestens 3 Monate vorher schriftlich mitgeteilt werden. Erfolgt die
        Anzeige verspätet, so ist sie erst zum nächsten Austrittstermin wirksam. Für
        die Rechtzeitigkeit ist das Datum der Postaufgabe maßgeblich.
      </li>
      <li>
        Der Vorstand kann ein Mitglied ausschließen, wenn dieses trotz zweimaliger
        schriftlicher Mahnung unter Setzung einer angemessenen Nachfrist länger als
        sechs Monate mit der Zahlung der Mitgliedsbeiträge im Rückstand ist. Die
        Verpflichtung zur Zahlung der fällig gewordenen Mitgliedsbeiträge bleibt hievon
        unberührt.
      </li>
      <li>
        Der Ausschluss eines Mitglieds aus dem Verein kann vom Vorstand auch wegen
        grober Verletzung anderer Mitgliedspflichten und wegen unehrenhaften Verhaltens
        verfügt werden.
      </li>
      <li>
        Die Aberkennung der Ehrenmitgliedschaft kann aus den im Abs. 4 genannten
        Gründen von der Generalversammlung über Antrag des Vorstands beschlossen
        werden.
      </li>
    </ol>

    <h2>§ 7 Rechte und Pflichten der Mitglieder</h2>
    <ol>
      <li>
        Die Mitglieder sind berechtigt, an allen Veranstaltungen des Vereins
        teilzunehmen und die Einrichtungen des Vereins zu beanspruchen. Das Stimmrecht
        in der Generalversammlung sowie das aktive und passive Wahlrecht steht nur den
        ordentlichen und den Ehrenmitgliedern zu.
      </li>
      <li>
        Jedes Mitglied ist berechtigt, vom Vorstand die Ausfolgung der Statuten zu
        verlangen.
      </li>
      <li>
        Mindestens ein Zehntel der Mitglieder kann vom Vorstand die Einberufung einer
        Generalversammlung verlangen.
      </li>
      <li>
        Die Mitglieder sind in jeder Generalversammlung vom Vorstand über die
        Tätigkeit und finanzielle Gebarung des Vereins zu informieren. Wenn mindestens
        ein Zehntel der Mitglieder dies unter Angabe von Gründen verlangt, hat der
        Vorstand den betreffenden Mitgliedern eine solche Information auch sonst binnen
        vier Wochen zu geben.
      </li>
      <li>
        Die Mitglieder sind vom Vorstand über den geprüften Rechnungsabschluss
        (Rechnungslegung) zu informieren. Geschieht dies in der Generalversammlung,
        sind die Rechnungsprüfer einzubinden.
      </li>
      <li>
        Die Mitglieder sind verpflichtet, die Interessen des Vereins nach Kräften zu
        fördern und alles zu unterlassen, wodurch das Ansehen und der Zweck des Vereins
        Abbruch erleiden könnte. Sie haben die Vereinsstatuten und die Beschlüsse der
        Vereinsorgane zu beachten. Die ordentlichen und außerordentlichen Mitglieder
        sind zur pünktlichen Zahlung der Beitrittsgebühr und der Mitgliedsbeiträge in
        der von der Generalversammlung beschlossenen Höhe verpflichtet.
      </li>
    </ol>

    <h2>§ 8 Vereinsorgane</h2>
    <p>
      Organe des Vereins sind die Generalversammlung (§§ 9 und 10), der Vorstand
      (§§ 11 bis 13), die Rechnungsprüfer (§ 14) und das Schiedsgericht (§ 15).
    </p>

    <h2>§ 9 Generalversammlung</h2>
    <ol>
      <li>
        Die Generalversammlung ist die „Mitgliederversammlung" im Sinne des
        Vereinsgesetzes 2002. Eine ordentliche Generalversammlung findet mindestens
        einmal jährlich statt.
      </li>
      <li>
        Eine außerordentliche Generalversammlung findet auf
        <ol type="a">
          <li>Beschluss des Vorstands oder der ordentlichen Generalversammlung,</li>
          <li>schriftlichen Antrag von mindestens einem Zehntel der Mitglieder,</li>
          <li>Verlangen der Rechnungsprüfer (§ 21 Abs. 5 erster Satz VereinsG),</li>
          <li>
            Beschluss der/eines Rechnungsprüfer/s (§ 21 Abs. 5 zweiter Satz VereinsG,
            § 11 Abs. 2 dritter Satz dieser Statuten),
          </li>
          <li>
            Beschluss eines gerichtlich bestellten Kurators (§ 11 Abs. 2 letzter Satz
            dieser Statuten)
          </li>
        </ol>
        binnen vier Wochen statt.
      </li>
      <li>
        Sowohl zu den ordentlichen wie auch zu den außerordentlichen
        Generalversammlungen sind alle Mitglieder mindestens zwei Wochen vor dem Termin
        schriftlich, mittels Telefax oder per E-Mail (an die vom Mitglied dem Verein
        bekanntgegebene Fax-Nummer oder E-Mail-Adresse) einzuladen. Die Anberaumung der
        Generalversammlung hat unter Angabe der Tagesordnung zu erfolgen. Die
        Einberufung erfolgt durch den Vorstand (Abs. 1 und Abs. 2 lit. a – c), durch
        die/einen Rechnungsprüfer (Abs. 2 lit. d) oder durch einen gerichtlich
        bestellten Kurator (Abs. 2 lit. e).
      </li>
      <li>
        Anträge zur Generalversammlung sind mindestens drei Tage vor dem Termin der
        Generalversammlung beim Vorstand schriftlich, mittels Telefax oder per E-Mail
        einzureichen.
      </li>
      <li>
        Gültige Beschlüsse – ausgenommen solche über einen Antrag auf Einberufung einer
        außerordentlichen Generalversammlung – können nur zur Tagesordnung gefasst
        werden.
      </li>
      <li>
        Bei der Generalversammlung sind alle Mitglieder teilnahmeberechtigt.
        Stimmberechtigt sind nur die ordentlichen und die Ehrenmitglieder. Jedes
        Mitglied hat eine Stimme. Die Übertragung des Stimmrechts auf ein anderes
        Mitglied im Wege einer schriftlichen Bevollmächtigung ist zulässig.
      </li>
      <li>
        Die Generalversammlung ist ohne Rücksicht auf die Anzahl der Erschienenen
        beschlussfähig.
      </li>
      <li>
        Die Wahlen und die Beschlussfassungen in der Generalversammlung erfolgen in der
        Regel mit einfacher Mehrheit der abgegebenen gültigen Stimmen. Beschlüsse, mit
        denen das Statut des Vereins geändert oder der Verein aufgelöst werden soll,
        bedürfen jedoch einer qualifizierten Mehrheit von zwei Dritteln der abgegebenen
        gültigen Stimmen.
      </li>
      <li>
        Den Vorsitz in der Generalversammlung führt der/die Obmann/Obfrau, in
        dessen/deren Verhinderung sein/e/ihr/e Stellvertreter/in. Wenn auch diese/r
        verhindert ist, so führt das an Jahren älteste anwesende Vorstandsmitglied den
        Vorsitz.
      </li>
    </ol>

    <h2>§ 10 Aufgaben der Generalversammlung</h2>
    <p>Der Generalversammlung sind folgende Aufgaben vorbehalten:</p>
    <ol>
      <li>Beschlussfassung über den Voranschlag;</li>
      <li>
        Entgegennahme und Genehmigung des Rechenschaftsberichts und des
        Rechnungsabschlusses unter Einbindung der Rechnungsprüfer;
      </li>
      <li>Wahl und Enthebung der Mitglieder des Vorstands und der Rechnungsprüfer;</li>
      <li>Genehmigung von Rechtsgeschäften zwischen Rechnungsprüfern und Verein;</li>
      <li>Entlastung des Vorstands;</li>
      <li>
        Festsetzung der Höhe der Beitrittsgebühr und der Mitgliedsbeiträge für
        ordentliche und für außerordentliche Mitglieder;
      </li>
      <li>Verleihung und Aberkennung der Ehrenmitgliedschaft;</li>
      <li>
        Beschlussfassung über Statutenänderungen und die freiwillige Auflösung des
        Vereins;
      </li>
      <li>
        Beratung und Beschlussfassung über sonstige auf der Tagesordnung stehende
        Fragen.
      </li>
    </ol>

    <h2>§ 11 Vorstand</h2>
    <ol>
      <li>
        Der Vorstand besteht aus mindestens drei Mitgliedern, und zwar aus
        Obmann/Obfrau, Obmann-Stellvertreter/in sowie Kassier/in.
      </li>
      <li>
        Der Vorstand wird von der Generalversammlung gewählt. Der Vorstand hat bei
        Ausscheiden eines gewählten Mitglieds das Recht, an seine Stelle ein anderes
        wählbares Mitglied zu kooptieren, wozu die nachträgliche Genehmigung in der
        nächstfolgenden Generalversammlung einzuholen ist. Fällt der Vorstand ohne
        Selbstergänzung durch Kooptierung überhaupt oder auf unvorhersehbar lange Zeit
        aus, so ist jeder Rechnungsprüfer verpflichtet, unverzüglich eine
        außerordentliche Generalversammlung zum Zweck der Neuwahl eines Vorstands
        einzuberufen. Sollten auch die Rechnungsprüfer handlungsunfähig sein, hat jedes
        ordentliche Mitglied, das die Notsituation erkennt, unverzüglich die Bestellung
        eines Kurators beim zuständigen Gericht zu beantragen, der umgehend eine
        außerordentliche Generalversammlung einzuberufen hat.
      </li>
      <li>
        Die Funktionsdauer des Vorstands beträgt drei Jahre. Erfolgt die Neuwahl nicht
        rechtzeitig vor ihrem Ablauf, so läuft sie bis zur Wahl eines neuen Vorstands
        weiter. Eine Wiederwahl ist möglich. Jede Funktion im Vorstand ist persönlich
        auszuüben.
      </li>
      <li>
        Der Vorstand wird vom Obmann/von der Obfrau, bei Verhinderung von
        seinem/seiner/ihrem/ihrer Stellvertreter/in, schriftlich oder mündlich
        einberufen. Ist auch diese/r auf unvorhersehbar lange Zeit verhindert, darf
        jedes sonstige Vorstandsmitglied den Vorstand einberufen.
      </li>
      <li>
        Der Vorstand ist beschlussfähig, wenn alle seine Mitglieder eingeladen wurden
        und mindestens die Hälfte von ihnen anwesend ist.
      </li>
      <li>
        Der Vorstand fasst seine Beschlüsse mit einfacher Stimmenmehrheit; bei
        Stimmengleichheit gibt die Stimme des/der Vorsitzenden den Ausschlag.
      </li>
      <li>
        Den Vorsitz führt der/die Obmann/Obfrau, bei Verhinderung sein/e/ihr/e
        Stellvertreter/in. Ist auch diese/r verhindert, obliegt der Vorsitz dem an
        Jahren ältesten anwesenden Vorstandsmitglied oder jenem Vorstandsmitglied, das
        die übrigen Vorstandsmitglieder mehrheitlich dazu bestimmen.
      </li>
      <li>
        Außer durch den Tod und Ablauf der Funktionsperiode (Abs. 3) erlischt die
        Funktion eines Vorstandsmitglieds durch Enthebung (Abs. 9) und Rücktritt
        (Abs. 10).
      </li>
      <li>
        Die Generalversammlung kann jederzeit den gesamten Vorstand oder einzelne
        seiner Mitglieder entheben. Die Enthebung tritt mit Bestellung des neuen
        Vorstands bzw Vorstandsmitglieds in Kraft.
      </li>
      <li>
        Die Vorstandsmitglieder können jederzeit schriftlich ihren Rücktritt erklären.
        Die Rücktrittserklärung ist an den Vorstand, im Falle des Rücktritts des
        gesamten Vorstands an die Generalversammlung zu richten. Der Rücktritt wird
        erst mit Wahl bzw Kooptierung (Abs. 2) eines Nachfolgers wirksam.
      </li>
    </ol>

    <h2>§ 12 Aufgaben des Vorstands</h2>
    <p>
      Dem Vorstand obliegt die Leitung des Vereins. Er ist das „Leitungsorgan" im Sinne
      des Vereinsgesetzes 2002. Ihm kommen alle Aufgaben zu, die nicht durch die
      Statuten einem anderen Vereinsorgan zugewiesen sind. In seinen Wirkungsbereich
      fallen insbesondere folgende Angelegenheiten:
    </p>
    <ol>
      <li>
        Einrichtung eines den Anforderungen des Vereins entsprechenden Rechnungswesens
        mit laufender Aufzeichnung der Einnahmen/Ausgaben und Führung eines
        Vermögensverzeichnisses als Mindesterfordernis;
      </li>
      <li>
        Erstellung des Jahresvoranschlags, des Rechenschaftsberichts und des
        Rechnungsabschlusses;
      </li>
      <li>
        Vorbereitung und Einberufung der Generalversammlung in den Fällen des § 9
        Abs. 1 und Abs. 2 lit. a – c dieser Statuten;
      </li>
      <li>
        Information der Vereinsmitglieder über die Vereinstätigkeit, die
        Vereinsgebarung und den geprüften Rechnungsabschluss;
      </li>
      <li>Verwaltung des Vereinsvermögens;</li>
      <li>Aufnahme und Ausschluss von ordentlichen und außerordentlichen Vereinsmitgliedern;</li>
      <li>Aufnahme und Kündigung von Angestellten des Vereins.</li>
    </ol>

    <h2>§ 13 Besondere Obliegenheiten einzelner Vorstandsmitglieder</h2>
    <ol>
      <li>
        Der/die Obmann/Obfrau führt die laufenden Geschäfte des Vereins. Der/die
        Kassier/Kassiererin unterstützt den/die Obmann/Obfrau bei der Führung der
        Vereinsgeschäfte.
      </li>
      <li>
        Der/die Obmann/Obfrau vertritt den Verein nach außen. Schriftliche
        Ausfertigungen des Vereins bedürfen zu ihrer Gültigkeit der Unterschriften
        des/der Obmanns/Obfrau und des Kassiers/der Kassiererin. In Geldangelegenheiten
        (vermögenswerte Dispositionen) zeichnen der/die Obmann/Obfrau und der/die
        Kassier/in gemeinsam. Für Beträge bis zu einer Höhe von EUR 500,- ist der/die
        Kassier/in jedoch einzelzeichnungsberechtigt.
      </li>
      <li>
        Rechtsgeschäftliche Bevollmächtigungen, den Verein nach außen zu vertreten bzw.
        für ihn zu zeichnen, können ausschließlich von den in Abs. 2 genannten
        Vorstandsmitgliedern erteilt werden.
      </li>
      <li>
        Bei Gefahr im Verzug ist der/die Obmann/Obfrau berechtigt, auch in
        Angelegenheiten, die in den Wirkungsbereich der Generalversammlung oder des
        Vorstands fallen, unter eigener Verantwortung selbständig Anordnungen zu
        treffen; im Innenverhältnis bedürfen diese jedoch der nachträglichen
        Genehmigung durch das zuständige Vereinsorgan.
      </li>
      <li>Der/die Obmann/Obfrau führt den Vorsitz in der Generalversammlung und im Vorstand.</li>
      <li>
        Der/die Kassier/in führt die Protokolle der Generalversammlung und des
        Vorstands oder sorgt für deren Führung.
      </li>
      <li>Der/die Kassier/in ist für die ordnungsgemäße Geldgebarung des Vereins verantwortlich.</li>
      <li>
        Im Falle der Verhinderung des Obmanns/der Obfrau sowie des Kassiers/der
        Kassierin übernimmt der Obmann-Stellvertreter/die Obmann-Stellvertreterin deren
        Vertretung.
      </li>
    </ol>

    <h2>§ 14 Rechnungsprüfer</h2>
    <ol>
      <li>
        Zwei Rechnungsprüfer werden von der Generalversammlung auf die Dauer von drei
        Jahren gewählt. Wiederwahl ist möglich. Die Rechnungsprüfer dürfen keinem
        Organ – mit Ausnahme der Generalversammlung – angehören, dessen Tätigkeit
        Gegenstand der Prüfung ist.
      </li>
      <li>
        Den Rechnungsprüfern obliegt die Prüfung der Finanzgebarung des Vereins im
        Hinblick auf die Ordnungsmäßigkeit der Rechnungslegung und die statutengemäße
        Verwendung der Mittel. Der Vorstand hat den Rechnungsprüfern die erforderlichen
        Unterlagen vorzulegen und die erforderlichen Auskünfte zu erteilen. Die
        Rechnungsprüfer haben dem Vorstand über das Ergebnis der Prüfung zu berichten.
      </li>
      <li>
        Rechtsgeschäfte zwischen Rechnungsprüfern und Verein bedürfen der Genehmigung
        durch die Generalversammlung. Im Übrigen gelten für die Rechnungsprüfer die
        Bestimmungen des § 11 Abs. 8 bis 10 sinngemäß.
      </li>
    </ol>

    <h2>§ 15 Schiedsgericht</h2>
    <ol>
      <li>
        Zur Schlichtung von allen aus dem Vereinsverhältnis entstehenden Streitigkeiten
        ist das vereinsinterne Schiedsgericht berufen. Es ist eine
        „Schlichtungseinrichtung" im Sinne des Vereinsgesetzes 2002 und kein
        Schiedsgericht nach den §§ 577 ff ZPO.
      </li>
      <li>
        Das Schiedsgericht setzt sich aus drei ordentlichen Vereinsmitgliedern
        zusammen. Es wird derart gebildet, dass ein Streitteil dem Vorstand ein
        Mitglied als Schiedsrichter schriftlich namhaft macht. Über Aufforderung durch
        den Vorstand binnen sieben Tagen macht der andere Streitteil innerhalb von 14
        Tagen seinerseits ein Mitglied des Schiedsgerichts namhaft. Nach Verständigung
        durch den Vorstand innerhalb von sieben Tagen wählen die namhaft gemachten
        Schiedsrichter binnen weiterer 14 Tage ein drittes ordentliches Mitglied
        zum/zur Vorsitzenden des Schiedsgerichts. Bei Stimmengleichheit entscheidet
        unter den Vorgeschlagenen das Los. Die Mitglieder des Schiedsgerichts dürfen
        keinem Organ – mit Ausnahme der Generalversammlung – angehören, dessen
        Tätigkeit Gegenstand der Streitigkeit ist.
      </li>
      <li>
        Das Schiedsgericht fällt seine Entscheidung nach Gewährung beiderseitigen
        Gehörs bei Anwesenheit aller seiner Mitglieder mit einfacher Stimmenmehrheit.
        Es entscheidet nach bestem Wissen und Gewissen. Seine Entscheidungen sind
        vereinsintern endgültig.
      </li>
    </ol>

    <h2>§ 16 Freiwillige Auflösung des Vereins</h2>
    <ol>
      <li>
        Die freiwillige Auflösung des Vereins kann nur in einer Generalversammlung und
        nur mit Zweidrittelmehrheit der abgegebenen gültigen Stimmen beschlossen werden.
      </li>
      <li>
        Diese Generalversammlung hat auch – sofern Vereinsvermögen vorhanden ist – über
        die Abwicklung zu beschließen. Insbesondere hat sie einen Abwickler zu berufen
        und Beschluss darüber zu fassen, wem dieser das nach Abdeckung der Passiven
        verbleibende Vereinsvermögen zu übertragen hat. Dieses Vermögen soll, soweit
        dies möglich und erlaubt ist, einer Organisation zufallen, die gleiche oder
        ähnliche Zwecke wie dieser Verein verfolgt, sonst Zwecken der Sozialhilfe.
      </li>
    </ol>
